class Lp4_tn // using term number
{
    public static void  main()
    {
        int tn, tv;
        //tn is term number
        //tv is term value
        for (tn=1; tn<=50; tn=tn+1)
        {
            tv=2*tn-1;
            System.out.println(tv);
        }//end of for loop
    }//end of main()
}//end of class