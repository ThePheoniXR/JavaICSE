//Write a program to generate series: 5, 10, 15, 20, ... 500.
class Lp6_tntv
{
    public static void main()
    {
        int tn, tv;
        for (tn=1; tn<=100; tn=tn+1)
        {
            tv=tn*5;
            System.out.println(tv);
        }//end of for loop
    }//end of main()
}//end of class